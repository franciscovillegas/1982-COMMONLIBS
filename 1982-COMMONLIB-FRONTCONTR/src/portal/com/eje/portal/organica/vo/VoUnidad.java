package portal.com.eje.portal.organica.vo;

public class VoUnidad {
	private Integer unid_empresa;
	private String unid_id;
	private String unid_desc;
	private String area;
	private String vigente;
	private Integer id_tipo;
	private String texto;
	private Integer dotacion_real;

	public Integer getUnid_empresa() {
		return unid_empresa;
	}

	public void setUnid_empresa(Integer unid_empresa) {
		this.unid_empresa = unid_empresa;
	}

	public String getUnid_id() {
		return unid_id;
	}

	public void setUnid_id(String unid_id) {
		this.unid_id = unid_id;
	}

	public String getUnid_desc() {
		return unid_desc;
	}

	public void setUnid_desc(String unid_desc) {
		this.unid_desc = unid_desc;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getVigente() {
		return vigente;
	}

	public void setVigente(String vigente) {
		this.vigente = vigente;
	}

	public Integer getId_tipo() {
		return id_tipo;
	}

	public void setId_tipo(Integer id_tipo) {
		this.id_tipo = id_tipo;
	}

	public String getTexto() {
		return texto;
	}

	public void setTexto(String texto) {
		this.texto = texto;
	}

	public Integer getDotacion_real() {
		return dotacion_real;
	}

	public void setDotacion_real(Integer dotacion_real) {
		this.dotacion_real = dotacion_real;
	}

}
